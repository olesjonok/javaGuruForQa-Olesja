package lv.javaguru.lessons.lesson3;

public class ExampleForLoop {
    public static void main(String[] args) {
        for (int j = 1; j < 11; j++) {
            System.out.println("j = " + j + "; ");
        }
        for (int j = 2; j < 5; j++) {
            System.out.println("j = " + j + "; ");
        }
    }
}
